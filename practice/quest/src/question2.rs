fn main(){
    let width = 5;
    let length = 7;
    let area = width*length;
    let perimeter = 2*(width+length);

    println!("The area of the rectangle with length: {} and width: {} is: {}", length, width, area);
    println!("The perimeter of the rectangle with length: {} and width: {} is: {}", length, width, perimeter);

}